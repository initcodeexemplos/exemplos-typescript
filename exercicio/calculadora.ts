export function somar(num1: number, num2: number) {
    return num1+num2;
}

export function sub(num1: number, num2: number) {
    return num1-num2;
}

export function mult(num1: number, num2: number) {
    return num1*num2;
}

export function div(num1: number, num2: number) {
    return num1/num2;
}